def main():
    # Countdown from 10 to 1
    # we use start stop and steps
    for i in range(10, 0, -1):  # Starts at 10, goes down to 1, step by -1
        print(i, end=" ")  # Print the number with a space

    # After the countdown, print "Liftoff!"
    print("Liftoff!")

# Run the program
main()
